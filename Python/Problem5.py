# Operator Precedence Evaluation
#
# Problem:
# Evaluate an arithmetic expression using Python's
# operator precedence rules and print the result.
#
# Approach:
# 1. Evaluate the exponent operation (**).
# 2. Perform multiplication (*).
# 3. Perform addition (+).
# 4. Store and print the final result.
#
# Time Complexity: O(1)
# Space Complexity: O(1)

x = 10 + 3 * 2 ** 2
print(x)